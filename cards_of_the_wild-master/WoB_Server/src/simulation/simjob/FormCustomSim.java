    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        System.out.println("You pressed Load Previous Job");
        String jobStr = JOptionPane.showInputDialog(null,
                "\nWhat job ID do you want to load?\n",
                "Load Previous Job", JOptionPane.YES_NO_OPTION);
        if (!jobStr.isEmpty()) {
            //setVisible(false);
            SimJob job = null;
            //9/25/14, JTC, added flag saveAsNew to indicate that job should be
            //loaded as a new job rather than keeping existing job ID
            boolean saveAsNew = true;
            try {
                job = jobMgr.createSimJobFromPrior(Integer.parseInt(jobStr.trim()), saveAsNew);
            } catch (SQLException ex) {
                Logger.getLogger(FormCustomSim.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (job != null) {
                jTextField3.setText(String.valueOf(job.getTimesteps()));

                SimJobSZT pp = job.getSpeciesZoneByNodeId(Constants.PP_NODE_ID);

                jTextField4.setText(String.valueOf(pp.getCurrentBiomass()));

                jTextField5.setText(String.valueOf(pp.getPerSpeciesBiomass()));

                if (pp.getParamK() != Constants.PARAM_INITVALUE) {
                    jTextField6.setText(String.valueOf(pp.getParamK()));
                } else {
                    jTextField6.setText(String.valueOf(SimJob.DFLT_PP_PARAMK));
                }

                if (pp.getParamX() != Constants.PARAM_INITVALUE) {
                    jTextField7.setText(String.valueOf(pp.getParamX()));
                } else {
                    jTextField7.setText(String.valueOf(SimJob.DFLT_PP_PARAMX));
                }

                if (pp.getParamR() != Constants.PARAM_INITVALUE) {
                    jTextField2.setText(String.valueOf(pp.getParamR()));
                } else {
                    jTextField2.setText(String.valueOf(SimJob.DFLT_PP_PARAMR));
                }

                if (job.getJob_Id() == SimJob.NO_ID) {
                    jFormattedTextField1.setText("Src job " + jobStr + ", " + job.getJob_Descript().trim());
                } else {
                    jFormattedTextField1.setText(job.getJob_Descript().trim());
                }

                //the following global params always initialize to blank.
                jTextField8.setText("");
                jTextField9.setText("");
                jTextField10.setText("");
                jTextField11.setText("");
                jTextField12.setText("");
                jTextField13.setText("");
                jTextField14.setText("");
                jCheckBox1.setSelected(false);
                jCheckBox2.setSelected(false);

                jTextArea1.setText(job.getNode_Config());
                repaint();
            } else {
                JOptionPane.showMessageDialog(null, "\nFailed to load job " + jobStr + ".\n",
                        "Load Previous Job", JOptionPane.ERROR_MESSAGE);
            }
            setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        System.out.println("You pressed Save Simulation in Custom Simulation");
        try {
        //save data
        SimJob job = jobMgr.getSimJob();
        if (job != null) {
            updateJobFromFields(job);
            repaint();

            //setVisible(false);
            int jobId = jobMgr.getSimJob().saveJob();
            if (jobId != SimJob.NO_ID) {
                System.out.printf("Custom Simulation - saved job ID: %d\n", jobId);
                JOptionPane.showMessageDialog(null, "Saved job ID " + jobId,
                        "Custom Simulation Saved", JOptionPane.INFORMATION_MESSAGE);
            }
            //9/25/14, jtc, now reset job ID so that further changes will be 
            //saved to a new record
            jobMgr.getSimJob().setJob_Id(SimJob.NO_ID);
        }

        } catch (SQLException ex) {
            Logger.getLogger(FormCustomSim.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

