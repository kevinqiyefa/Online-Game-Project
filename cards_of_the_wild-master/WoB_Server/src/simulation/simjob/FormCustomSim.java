    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        System.out.println("You pressed Run Simulation in Custom Simulation");
        //save data
        SimJob job = new SimJob();
        jobMgr.setSimJob(job);
        if (job != null) {
            updateJobFromFields(job);
            repaint();

            setVisible(false);
            int jobId = -1;
            try {
            jobId = jobMgr.runSimJob();
            } catch (SQLException ex) {
                Logger.getLogger(SimJobMenu.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SimulationException ex) {
                Logger.getLogger(FormCustomSim.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (jobId >= 0) {
                System.out.printf("Custom Simulation: created job ID: %d\n", jobId);
                JOptionPane.showMessageDialog(null, "Created job ID " + jobId,
                        "Custom Simulation Generated", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

