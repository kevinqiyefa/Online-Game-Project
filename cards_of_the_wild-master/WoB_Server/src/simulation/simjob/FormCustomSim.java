    private void updateJobFromFields(SimJob job) {
        job.setJob_Descript(jFormattedTextField1.getText().trim());

        /*note: Run setNode_Config() BEFORE primary producer and global param settings, as it would
         overwrite otherwise; primary producer settings and global param settings override
         node-config */
        if (! (job.getNode_Config().equals(jTextArea1.getText())
                && jTextField8.getText().isEmpty() && jTextField9.getText().isEmpty()
                && jTextField10.getText().isEmpty())) {
            job.setNode_Config(jTextArea1.getText());
        }

        if (jTextField3.getText().isEmpty()) {
            job.setTimesteps(SimJob.DFLT_TIMESTEPS);
        } else {
            job.setTimesteps(Integer.parseInt(jTextField3.getText().trim()));
        }

        //set primary producer parameters
        SimJobSZT pp = job.getSpeciesZoneByNodeId(Constants.PP_NODE_ID);

        if (jTextField4.getText().isEmpty()) {
            pp.setCurrentBiomass(SimJob.DFLT_PP_TOTAL_BIOMASS);
        } else {
            pp.setCurrentBiomass(Double.parseDouble(jTextField4.getText().trim()));
        }

        if (jTextField5.getText().isEmpty()) {
            pp.setPerSpeciesBiomass(SimJob.DFLT_PP_PER_UNIT_BIOMASS);
        } else {
            pp.setPerSpeciesBiomass(Double.parseDouble(jTextField5.getText().trim()));
        }

        if (jTextField6.getText().isEmpty()) {
            pp.setParamK(SimJob.DFLT_PP_PARAMK);
        } else {
            pp.setParamK(Double.parseDouble(jTextField6.getText().trim()));
        }

        if (jTextField7.getText().isEmpty()) {
            pp.setParamX(SimJob.DFLT_PP_PARAMX);
        } else {
            pp.setParamX(Double.parseDouble(jTextField7.getText().trim()));
        }

        if (jTextField2.getText().isEmpty()) {
            pp.setParamR(SimJob.DFLT_PP_PARAMR);
        } else {
            pp.setParamR(Double.parseDouble(jTextField2.getText().trim()));
        }

        //if any non-pp plant values are non-zero, update plant values in sztList
        //note, these values will override any changes made in node_config
        if (!(jTextField8.getText().isEmpty() && jTextField9.getText().isEmpty()
                && jTextField10.getText().isEmpty())) {
            double paramK = jTextField8.getText().isEmpty() ? Constants.PARAM_INITVALUE
                    : Double.valueOf(jTextField8.getText());
            double paramR = jTextField9.getText().isEmpty() ? Constants.PARAM_INITVALUE
                    : Double.valueOf(jTextField9.getText());
            double paramX = jTextField10.getText().isEmpty() ? Constants.PARAM_INITVALUE
                    : Double.valueOf(jTextField10.getText());
            job.setSpeciesZoneListParamKRX(paramK, paramR, paramX);
        }
        
        //if user has checked for ParamY calculation, calculate for all species
        if (jCheckBox1.isSelected()) {
            job.setSpeciesZoneListParamY();
        }

        //if user has checked for Metabolic Rate calculation, calculate for all animal species
        if (jCheckBox2.isSelected()) {
            job.setSpeciesZoneListAnimalParamX();
        }

        //update node config field to reflect pp and global plant settings
        try {
            jTextArea1.setText(job.buildNodeConfig());
        } catch (Exception ex) {
            Logger.getLogger(FormCustomSim.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


